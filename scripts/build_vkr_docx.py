def build_document(template_path: Path, output_path: Path) -> None:
    with ZipFile(template_path, "r") as zin:
        root = ET.fromstring(zin.read("word/document.xml"))
        body = root.find(w("body"))
        if body is None:
            raise RuntimeError("word/document.xml does not contain w:body")

        for text_node in root.findall(".//" + w("t")):
            if text_node.text == "_____________________________________________________________":
                text_node.text = THEME
            elif text_node.text == "наименование темы выпускной квалификационной работы":
                text_node.text = ""

        sect = body.find(w("sectPr"))
        if sect is None:
            raise RuntimeError("word/document.xml does not contain w:sectPr")
        insert_at = list(body).index(sect)

        nodes_to_add: list[ET.Element] = [make_page_break(), make_heading("СОДЕРЖАНИЕ")]
        nodes_to_add.extend(make_content_entry(entry) for entry in CONTENT_ENTRIES)
        nodes_to_add.extend([make_page_break(), make_heading("ВВЕДЕНИЕ")])
        nodes_to_add.extend(make_paragraph(text) for text in INTRO_PARAGRAPHS)
        nodes_to_add.extend([make_page_break(), make_heading("ГЛАВА 1. ИССЛЕДОВАТЕЛЬСКАЯ")])
        for title, paragraphs in CHAPTER_1:
            nodes_to_add.append(make_subheading(title))
            nodes_to_add.extend(make_paragraph(text) for text in paragraphs)

        for offset, node in enumerate(nodes_to_add):
            body.insert(insert_at + offset, node)

        document_xml = ET.tostring(root, encoding="utf-8", xml_declaration=True)

        with ZipFile(output_path, "w", ZIP_DEFLATED) as zout:
            for item in zin.infolist():
                data = document_xml if item.filename == "word/document.xml" else zin.read(item.filename)
                zout.writestr(item, data)


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--template", required=True)
    parser.add_argument("--output", required=True)
    args = parser.parse_args()

    build_document(Path(args.template), Path(args.output))


if __name__ == "__main__":
    main()
