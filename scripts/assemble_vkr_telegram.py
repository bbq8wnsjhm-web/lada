NS_W = "http://schemas.openxmlformats.org/wordprocessingml/2006/main"
XML_SPACE = "{http://www.w3.org/XML/1998/namespace}space"

ET.register_namespace("w", NS_W)


def w(tag: str) -> str:
    return f"{{{NS_W}}}{tag}"


def make_run(text: str, *, bold: bool = False, size: str = "24") -> ET.Element:
    run = ET.Element(w("r"))
    run_props = ET.SubElement(run, w("rPr"))
    run_fonts = ET.SubElement(run_props, w("rFonts"))
    for attr in ("ascii", "hAnsi", "eastAsia", "cs"):
        run_fonts.set(w(attr), "Times New Roman")
    if bold:
        ET.SubElement(run_props, w("b"))
        ET.SubElement(run_props, w("bCs"))
    size_node = ET.SubElement(run_props, w("sz"))
    size_node.set(w("val"), size)
    size_cs_node = ET.SubElement(run_props, w("szCs"))
    size_cs_node.set(w("val"), size)
    text_node = ET.SubElement(run, w("t"))
    if text.startswith(" ") or text.endswith(" "):
        text_node.set(XML_SPACE, "preserve")
    text_node.text = text
    return run


def make_paragraph(
    text: str | None = None,
    *,
    align: str = "both",
    first_line: str | None = "708",
    bold: bool = False,
    size: str = "24",
    left: str | None = None,
) -> ET.Element:
    paragraph = ET.Element(w("p"))
    props = ET.SubElement(paragraph, w("pPr"))
    spacing = ET.SubElement(props, w("spacing"))
    spacing.set(w("before"), "0")
    spacing.set(w("after"), "0")
    spacing.set(w("line"), "360")
    spacing.set(w("lineRule"), "auto")
    if first_line is not None or left is not None:
        indent = ET.SubElement(props, w("ind"))
        if first_line is not None:
            indent.set(w("firstLine"), first_line)
        if left is not None:
            indent.set(w("left"), left)
    justification = ET.SubElement(props, w("jc"))
    justification.set(w("val"), align)
    if text is not None:
        paragraph.append(make_run(text, bold=bold, size=size))
    return paragraph


def make_heading(text: str) -> ET.Element:
    return make_paragraph(text, align="center", first_line=None, bold=True, size="28")


def make_subheading(text: str) -> ET.Element:
    return make_paragraph(text, align="left", first_line=None, bold=True, size="24")


def make_list_item(text: str) -> ET.Element:
    return make_paragraph(text, align="left", first_line=None, left="567")


def make_page_break() -> ET.Element:
    paragraph = ET.Element(w("p"))
    run = ET.SubElement(paragraph, w("r"))
    br = ET.SubElement(run, w("br"))
    br.set(w("type"), "page")
    return paragraph


